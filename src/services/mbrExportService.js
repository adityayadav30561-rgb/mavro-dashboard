async function collectBlogs(start, end) {
  const blogs = await Blog.find({
    status: 'published',
    publishedAt: { $gte: start, $lte: end },
  })
    .populate('targetWebsite', 'name slug')
    .select('title slug publishedAt targetWebsite')
    .sort({ publishedAt: -1 })
    .lean();

  // All-time views per blog page (page_view + blog_view on /blog/<slug>)
  const pageCounts = await AnalyticsEvent.aggregate([
    { $match: { eventType: { $in: ['page_view', 'blog_view'] }, page: /\/blog\// } },
    { $group: { _id: '$page', count: { $sum: 1 } } },
  ]);
  const viewsForSlug = (slug) => {
    let total = 0;
    for (const p of pageCounts) {
      const path = String(p._id).split('?')[0].replace(/\/$/, '');
      if (path.endsWith(`/blog/${slug}`)) total += p.count;
    }
    return total;
  };

  return blogs.map((b) => ({
    title: b.title,
    slug: b.slug,
    tenant: b.targetWebsite?.name || b.targetWebsite?.slug || '—',
    publishedAt: b.publishedAt,
    views: viewsForSlug(b.slug),
  }));
}

async function collectLeads(start, end) {
  return Lead.find({ createdAt: { $gte: start, $lte: end }, isSpam: { $ne: true } })
    .populate('website', 'name slug')
    .select('name email phone company message formId customFields sourcePage website createdAt')
    .sort({ createdAt: -1 })
    .lean();
}

