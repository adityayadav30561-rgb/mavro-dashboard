const C = {
  ink: 'FF241F18',        // warm near-black
  navy: 'FF102A43',       // header band
  white: 'FFFFFFFF',
  cream: 'FFF3EDE0',      // section banner
  paper: 'FFFBF8F1',      // zebra light
  vermilion: 'FFC2431F',  // accents / deltas down
  green: 'FF3F6212',      // deltas up
  border: 'FFD8CDB8',
  muted: 'FF6B6155',
};

const thin = { style: 'thin', color: { argb: C.border } };
const BORDER = { top: thin, left: thin, bottom: thin, right: thin };

function addTitle(ws, title, subtitle, width) {
  const t = ws.addRow([title]);
  ws.mergeCells(t.number, 1, t.number, width);
  t.getCell(1).font = { name: 'Georgia', size: 16, bold: true, color: { argb: C.ink } };
  t.height = 26;
  if (subtitle) {
    const s = ws.addRow([subtitle]);
    ws.mergeCells(s.number, 1, s.number, width);
    s.getCell(1).font = { name: 'Calibri', size: 10, italic: true, color: { argb: C.muted } };
  }
  ws.addRow([]);
}

function addSectionBanner(ws, text, width) {
  const r = ws.addRow([text]);
  ws.mergeCells(r.number, 1, r.number, width);
  const cell = r.getCell(1);
  cell.font = { name: 'Calibri', size: 11, bold: true, color: { argb: C.ink } };
  cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: C.cream } };
  cell.border = BORDER;
  r.height = 20;
}

function addHeaderRow(ws, labels) {
  const r = ws.addRow(labels);
  r.eachCell((cell) => {
    cell.font = { name: 'Calibri', size: 10, bold: true, color: { argb: C.white } };
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: C.navy } };
    cell.border = BORDER;
    cell.alignment = { vertical: 'middle', wrapText: true };
  });
  r.height = 18;
  return r;
}

function addDataRow(ws, values, { zebra = false, wrap = true } = {}) {
  const r = ws.addRow(values);
  r.eachCell({ includeEmpty: true }, (cell) => {
    cell.font = { name: 'Calibri', size: 10, color: { argb: C.ink } };
    cell.border = BORDER;
    cell.alignment = { vertical: 'top', wrapText: wrap };
    if (zebra) cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: C.paper } };
  });
  return r;
}

function deltaCellStyle(cell, delta) {
  if (delta == null || Number.isNaN(delta)) return;
  cell.font = { name: 'Calibri', size: 10, bold: true, color: { argb: delta >= 0 ? C.green : C.vermilion } };
}

const fmtPct = (v) => (v == null || Number.isNaN(v) ? '—' : `${v >= 0 ? '+' : ''}${Math.round(v * 100)}%`);
const pctOf = (cur, prev) => (prev > 0 ? (cur - prev) / prev : null);
const fmtDur = (sec) => {
  if (!sec || sec < 1) return '0s';
  const m = Math.floor(sec / 60);
  const s = Math.round(sec % 60);
  return m ? `${m}m ${s}s` : `${s}s`;
};
const fmtDate = (d) => (d ? new Date(d).toISOString().slice(0, 10) : '');

