function buildSourceSheet(wb, label, ga4, gsc, rangeLabel, periodLabels) {
  const ws = wb.addWorksheet(label.slice(0, 31));
  ws.columns = [
    { width: 30 }, { width: 15 }, { width: 15 }, { width: 15 }, { width: 11 }, { width: 11 }, { width: 34 },
  ];
  const L = periodLabels || { current: 'Current', previous: 'Previous', previous2: 'Previous-2' };
  addTitle(ws, `${label} — Traffic, Search & Conversion`, `Period: ${rangeLabel} · 3-month comparison · Generated by Mavro MBR`, 7);

  if (!ga4) {
    addDataRow(ws, ['GA4 not configured / not reachable for this source.']);
  } else {
    const ov = ga4.overview;
    const ov2 = ov.previous2 || {};
    addSectionBanner(ws, 'A. Audience & Engagement (GA4)', 7);
    addHeaderRow(ws, ['Metric', L.current, L.previous, L.previous2, 'MoM Δ', 'MoM %', 'Notes']);
    const metrics = [
      ['Total users', ov.current.users, ov.previous.users, ov2.users],
      ['New users', ov.current.newUsers, ov.previous.newUsers, ov2.newUsers],
      ['Sessions', ov.current.sessions, ov.previous.sessions, ov2.sessions],
      ['Engaged sessions', ov.current.engagedSessions, ov.previous.engagedSessions, ov2.engagedSessions],
      ['Engagement rate', `${Math.round(ov.current.engagementRate * 100)}%`, `${Math.round(ov.previous.engagementRate * 100)}%`, `${Math.round((ov2.engagementRate || 0) * 100)}%`, ov.current.engagementRate - ov.previous.engagementRate],
      ['Avg engagement / user', fmtDur(ov.current.avgEngagementSec), fmtDur(ov.previous.avgEngagementSec), fmtDur(ov2.avgEngagementSec), ov.current.avgEngagementSec - ov.previous.avgEngagementSec],
      ['Page views', ov.current.pageViews, ov.previous.pageViews, ov2.pageViews],
    ];
    metrics.forEach(([name, cur, prev, prev2, rawDelta], i) => {
      const numeric = typeof cur === 'number';
      const delta = rawDelta !== undefined ? rawDelta : (numeric ? cur - prev : null);
      const pct = numeric ? pctOf(cur, prev) : null;
      const r = addDataRow(ws, [name, cur, prev, prev2 ?? 0, delta == null ? '—' : (typeof delta === 'number' ? Math.round(delta * 100) / 100 : delta), fmtPct(pct), ''], { zebra: i % 2 === 1 });
      deltaCellStyle(r.getCell(5), typeof delta === 'number' ? delta : null);
      deltaCellStyle(r.getCell(6), pct);
    });
    ws.addRow([]);

    addSectionBanner(ws, 'B. Channel Mix (GA4, current period)', 7);
    addHeaderRow(ws, ['Channel', 'Sessions', 'Users', '', '', '', '']);
    ga4.channels.forEach((c, i) => addDataRow(ws, [c.channel, c.sessions, c.users, '', '', '', ''], { zebra: i % 2 === 1 }));
    ws.addRow([]);

    addSectionBanner(ws, 'C. AI Assistant Referrals — the GEO scoreboard', 7);
    addHeaderRow(ws, ['AI source', 'Sessions', 'Users', '', '', '', '']);
    if (ga4.aiReferrals.sources.length === 0) {
      addDataRow(ws, ['No AI-referred sessions this period', '', '', '', '', '', '']);
    } else {
      ga4.aiReferrals.sources.forEach((s, i) => addDataRow(ws, [s.source, s.sessions, s.users, '', '', '', ''], { zebra: i % 2 === 1 }));
    }
    const aiTotal = addDataRow(ws, ['TOTAL', ga4.aiReferrals.currentSessions, ga4.aiReferrals.previousSessions, ga4.aiReferrals.previous2Sessions || 0, '', '', `${L.current} | ${L.previous} | ${L.previous2}`]);
    aiTotal.getCell(1).font = { name: 'Calibri', size: 10, bold: true, color: { argb: C.ink } };
    ws.addRow([]);

    addSectionBanner(ws, 'D. Conversion Events (GA4)', 7);
    addHeaderRow(ws, ['Event', L.current, L.previous, L.previous2, 'MoM Δ', 'MoM %', '']);
    Object.entries(ga4.events).forEach(([event, e], i) => {
      const pct = pctOf(e.current, e.previous);
      const r = addDataRow(ws, [event, e.current, e.previous, e.previous2 || 0, e.current - e.previous, fmtPct(pct), ''], { zebra: i % 2 === 1 });
      deltaCellStyle(r.getCell(5), e.current - e.previous);
      deltaCellStyle(r.getCell(6), pct);
    });
    ws.addRow([]);

    if (ga4.fileDownloads.length > 0) {
      addSectionBanner(ws, 'E. Brochure / File Downloads', 7);
      addHeaderRow(ws, ['File', 'Downloads', '', '', '', '', '']);
      ga4.fileDownloads.forEach((f, i) => addDataRow(ws, [f.file, f.count, '', '', '', '', ''], { zebra: i % 2 === 1 }));
      ws.addRow([]);
    }

    addSectionBanner(ws, 'F. Top Pages (GA4, current period)', 7);
    addHeaderRow(ws, ['Page', 'Views', 'Users', 'Avg time', '', '', '']);
    ga4.topPages.slice(0, 15).forEach((p, i) =>
      addDataRow(ws, [p.path, p.views, p.users, fmtDur(p.avgEngagementSec), '', '', ''], { zebra: i % 2 === 1 }));
    ws.addRow([]);

    addSectionBanner(ws, 'G. Geography — Top Countries', 7);
    addHeaderRow(ws, ['Country', 'Users', 'Sessions', '', '', '', '']);
    (ga4.countries || []).filter((c) => c.country !== '(not set)').slice(0, 10)
      .forEach((c, i) => addDataRow(ws, [c.country, c.users, c.sessions, '', '', '', ''], { zebra: i % 2 === 1 }));
    ws.addRow([]);
  }

  if (gsc) {
    addSectionBanner(ws, 'H. Google Search Performance (Search Console)', 7);
    addHeaderRow(ws, ['Metric', L.current, L.previous, L.previous2, 'MoM Δ', 'MoM %', 'Notes']);
    const t = gsc.totals;
    const t2 = t.previous2 || {};
    const rows = [
      ['Clicks', t.current.clicks, t.previous.clicks, t2.clicks],
      ['Impressions', t.current.impressions, t.previous.impressions, t2.impressions],
      ['CTR', `${(t.current.ctr * 100).toFixed(1)}%`, `${(t.previous.ctr * 100).toFixed(1)}%`, `${((t2.ctr || 0) * 100).toFixed(1)}%`, t.current.ctr - t.previous.ctr],
      ['Avg position', t.current.position, t.previous.position, t2.position || 0, t.previous.position ? t.previous.position - t.current.position : null, null, 'lower is better'],
    ];
    rows.forEach(([name, cur, prev, prev2, rawDelta, _p, note], i) => {
      const numeric = typeof cur === 'number';
      const delta = rawDelta !== undefined ? rawDelta : (numeric ? cur - prev : null);
      const pct = numeric && name !== 'Avg position' ? pctOf(cur, prev) : null;
      const r = addDataRow(ws, [name, cur, prev, prev2 ?? 0, delta == null ? '—' : Math.round(delta * 100) / 100, fmtPct(pct), note || ''], { zebra: i % 2 === 1 });
      deltaCellStyle(r.getCell(5), typeof delta === 'number' ? delta : null);
      deltaCellStyle(r.getCell(6), pct);
    });
    ws.addRow([]);

    addSectionBanner(ws, 'I. Top Search Queries', 7);
    addHeaderRow(ws, ['Query', 'Clicks', 'Impressions', 'CTR', 'Position', '', '']);
    gsc.topQueries.slice(0, 15).forEach((q, i) =>
      addDataRow(ws, [q.query, q.clicks, q.impressions, `${(q.ctr * 100).toFixed(1)}%`, q.position, '', ''], { zebra: i % 2 === 1 }));
    ws.addRow([]);

    addSectionBanner(ws, 'J. Top Pages in Google', 7);
    addHeaderRow(ws, ['Page', 'Clicks', 'Impressions', 'CTR', 'Position', '', '']);
    gsc.topPages.slice(0, 12).forEach((p, i) =>
      addDataRow(ws, [p.page.replace(/^https?:\/\/[^/]+/, '') || '/', p.clicks, p.impressions, `${(p.ctr * 100).toFixed(1)}%`, p.position, '', ''], { zebra: i % 2 === 1 }));
  } else {
    addSectionBanner(ws, 'H. Google Search Performance', 7);
    addDataRow(ws, ['Search Console not configured for this source.']);
  }

  return ws;
}

function buildManualSheet(wb, sectionKey, items) {
  const def = sectionByKey(sectionKey);
  const ws = wb.addWorksheet(def.sheet.slice(0, 31));
  const cols = def.columns;
  ws.columns = [{ width: 5 }, ...cols.map((c) => ({ width: c.width }))];
  addTitle(ws, def.sheet, def.description, cols.length + 1);

  addHeaderRow(ws, ['#', ...cols.map((c) => c.label)]);
  if (items.length === 0) {
    addDataRow(ws, ['—', 'No entries recorded for this period', ...cols.slice(1).map(() => '')]);
  } else {
    items.forEach((item, i) => {
      addDataRow(ws, [i + 1, ...cols.map((c) => item.data?.[c.key] ?? '')], { zebra: i % 2 === 1 });
    });
  }
  return ws;
}

