/**
 * @param {{current: {startDate, endDate}, previous: {startDate, endDate}, label: string}} ranges
 * @param {string} period 'YYYY-MM' key for manual items
 * @returns {Promise<ExcelJS.Workbook>}
 */
async function buildWorkbook(ranges, period) {
  const wb = new ExcelJS.Workbook();
  wb.creator = 'Mavro MBR';
  wb.created = new Date();

  const rangeLabel = `${ranges.current.startDate} → ${ranges.current.endDate}`;
  const start = new Date(`${ranges.current.startDate}T00:00:00Z`);
  const end = new Date(`${ranges.current.endDate}T23:59:59.999Z`);

  // Pull everything up front (sources sequential to be gentle on quotas)
  const sources = getSources();
  const sourceReports = [];
  for (const src of sources) {
    let ga4 = null;
    let gsc = null;
    if (ga4Service.isConfigured(src.ga4PropertyId) && src.ga4PropertyId) {
      try { ga4 = await ga4Service.getMbrReport(ranges, src.ga4PropertyId, getHostScope(src), src.label); }
      catch (err) { console.error(`[mbrExport] GA4 failed for ${src.key}:`, err.message); }
    }
    if (gscService.isConfigured(src.gscSiteUrl) && src.gscSiteUrl) {
      try { gsc = await gscService.getMbrReport(ranges, src.gscSiteUrl); }
      catch (err) { console.error(`[mbrExport] GSC failed for ${src.key}:`, err.message); }
    }
    sourceReports.push({ src, ga4, gsc });
  }

  const [blogs, leads, manualItems] = await Promise.all([
    collectBlogs(start, end),
    collectLeads(start, end),
    MbrItem.find({ period }).sort({ section: 1, order: 1, createdAt: 1 }).lean(),
  ]);
  const itemsBySection = (key) => manualItems.filter((i) => i.section === key);

  // ── Sheet 1: Work Overview ────────────────────────────────────────────
  const ov = wb.addWorksheet('Work Overview');
  ov.columns = [{ width: 5 }, { width: 18 }, { width: 24 }, { width: 60 }, { width: 22 }];
  addTitle(ov, 'SaiSatwik Technologies — Monthly Business Review', `Period: ${rangeLabel} · Generated ${new Date().toISOString().slice(0, 10)} by Mavro`, 5);
  addHeaderRow(ov, ['#', 'Domain', 'Workstream', 'Coverage', 'Data status']);
  let n = 0;
  sourceReports.forEach(({ src, ga4, gsc }) => {
    n += 1;
    addDataRow(ov, [n, src.label, 'Traffic · Search · Conversion', `GA4 + Search Console for ${src.label}`, ga4 || gsc ? '✅ Auto (live)' : '⚠️ Not configured'], { zebra: n % 2 === 0 });
  });
  n += 1;
  addDataRow(ov, [n, 'Content', 'Blogs published', `${blogs.length} post(s) in period, with all-time views`, '✅ Auto (live)'], { zebra: n % 2 === 0 });
  MBR_SECTIONS.forEach((s) => {
    n += 1;
    const count = itemsBySection(s.key).length;
    addDataRow(ov, [n, 'Manual', s.label, s.description, count > 0 ? `📝 ${count} entr${count === 1 ? 'y' : 'ies'}` : '⏳ No entries'], { zebra: n % 2 === 0 });
  });
  n += 1;
  addDataRow(ov, [n, 'Leads', 'Leads Log', `${leads.length} website lead(s) in period + manual/LinkedIn entries`, '✅ Auto + manual'], { zebra: n % 2 === 0 });

  // ── Per-source sheets ─────────────────────────────────────────────────
  sourceReports.forEach(({ src, ga4, gsc }) => buildSourceSheet(wb, src.label, ga4, gsc, rangeLabel, ranges.labels));

  // ── Blogs sheet ───────────────────────────────────────────────────────
  const bs = wb.addWorksheet('Blogs');
  bs.columns = [{ width: 5 }, { width: 62 }, { width: 16 }, { width: 14 }, { width: 12 }];
  addTitle(bs, 'Blogs Published', `Period: ${rangeLabel} · views are all-time (page_view + blog_view)`, 5);
  addHeaderRow(bs, ['#', 'Title', 'Tenant', 'Published', 'Views']);
  if (blogs.length === 0) {
    addDataRow(bs, ['—', 'No blogs published in this period', '', '', '']);
  } else {
    blogs.forEach((b, i) => addDataRow(bs, [i + 1, b.title, b.tenant, fmtDate(b.publishedAt), b.views], { zebra: i % 2 === 1 }));
  }

  // ── Manual sheets (skip manual_leads — merged into Leads Log below) ──
  ['work_log', 'ppts_videos', 'other_projects'].forEach((key) =>
    buildManualSheet(wb, key, itemsBySection(key)));

  // ── Leads Log (auto + manual merged) ─────────────────────────────────
  const ls = wb.addWorksheet('Leads Log');
  ls.columns = [
    { width: 5 }, { width: 14 }, { width: 22 }, { width: 30 }, { width: 15 },
    { width: 30 }, { width: 50 }, { width: 20 }, { width: 12 },
  ];
  addTitle(ls, 'Leads Log', `Period: ${rangeLabel} · website forms (auto) + manual/LinkedIn entries`, 9);
  addHeaderRow(ls, ['#', 'Date', 'Name', 'Email', 'Phone', 'Service / Interest', 'Notes', 'Form / Source', 'Channel']);
  const autoRows = leads.map((l) => ({
    date: l.createdAt,
    name: l.name || '',
    email: l.email || '',
    phone: l.phone || '',
    service: l.customFields?.interest || l.customFields?.audience || l.customFields?.service || '',
    notes: (l.message || '').slice(0, 300),
    form: l.formId || '',
    channel: 'WEBSITE',
  }));
  const manualRows = itemsBySection('manual_leads').map((i) => ({
    date: i.data?.date ? new Date(i.data.date) : null,
    name: i.data?.name || '',
    email: i.data?.contact || '',
    phone: '',
    service: i.data?.service || '',
    notes: i.data?.notes || '',
    form: i.data?.source || 'Manual',
    channel: (i.data?.source || 'MANUAL').toUpperCase().includes('LINKEDIN') ? 'LINKEDIN' : 'MANUAL',
  }));
  const allLeads = [...autoRows, ...manualRows].sort((a, b) => (b.date || 0) - (a.date || 0));
  if (allLeads.length === 0) {
    addDataRow(ls, ['—', 'No leads in this period', '', '', '', '', '', '', '']);
  } else {
    allLeads.forEach((l, i) =>
      addDataRow(ls, [i + 1, fmtDate(l.date), l.name, l.email, l.phone, l.service, l.notes, l.form, l.channel], { zebra: i % 2 === 1 }));
  }

  return wb;
}

