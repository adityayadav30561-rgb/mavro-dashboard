/**
 * MBR Excel export — builds the combined monthly workbook:
 *   Work Overview · one sheet per GA4/GSC source · Blogs · Work Log ·
 *   PPTs & Videos · Other Projects · Leads Log (DB + manual rows merged)
 *
 * Auto data comes from ga4Service/gscService (per-source) + Blog/Lead/
 * AnalyticsEvent collections; manual rows from MbrItem. Every sheet uses the
 * same style system so the download looks like a finished report, not a dump.
 */

const ExcelJS = require('exceljs');
const Blog = require('../models/Blog');
const Lead = require('../models/Lead');
const AnalyticsEvent = require('../models/AnalyticsEvent');
const MbrItem = require('../models/MbrItem');
const { MBR_SECTIONS, sectionByKey } = require('../config/mbrSections');
const ga4Service = require('./google/ga4Service');
const gscService = require('./google/gscService');
const { getSources, getHostScope } = require('./google/mbrSources');

// ---------------------------------------------------------------------------
